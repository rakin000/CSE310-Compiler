.CODE
PRINT PROC
	MOV BX, 8000H
	AND BX, AX
	CMP BX, 0
	JZ NOT_NEG

	MOV BX, AX
	MOV DL, 2DH
	MOV AH, 2
	INT 21H
	MOV AX, BX
	NEG AX

	NOT_NEG:
	LEA SI, NUM_STR
	ADD SI, 6
	
	PRINT_LOOP:
	DEC SI
	MOV DX, 0
	MOV CX, 10
	DIV CX
	ADD DL, '0'
	MOV [SI], DL

	CMP AX, 0
	JNE PRINT_LOOP
	MOV DX, SI 
	MOV AH, 9
	INT 21H

	MOV DL, 0AH
	MOV AH, 2
	INT 21H
	MOV DL, 0DH
	INT 21H
	RET
PRINT ENDP
